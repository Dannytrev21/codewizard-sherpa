# --- Seed-template predicates (AC-19, AC-15) ---------------------------------


def _template_carries_parent_commit_placeholder(parsed_json: dict[str, Any]) -> None:
    """AC-19 — seed template's last_indexed_commit is the placeholder string.

    Mutation: a contributor "tidies up" the template by replacing the
    placeholder with the actual prior commit SHA at fixture-creation
    time → this predicate fails (and the regenerate.sh substitution
    would no-op silently).
    """
    assert parsed_json.get("last_indexed_commit") == "PARENT_COMMIT", (
        f"_seed/scip-slice.template.json must carry the placeholder string "
        f"'PARENT_COMMIT' as last_indexed_commit (substituted at regen runtime); "
        f"got {parsed_json.get('last_indexed_commit')!r}"
    )


def _seed_template_counters_match_source_tree(parsed_json: dict[str, Any]) -> None:
    """AC-15(c) — files_indexed == files_in_repo == count(src/*.ts + main.ts).

    Mutation: contributor grows the source tree without updating the
    seed template → ``IndexHealthProbe`` would surface ``CoverageGap``
    instead of ``CommitsBehind`` and the adversarial fails for the
    wrong reason.
    """
    src_count = sum(1 for _ in (_FIXTURE / "src").glob("*.ts"))
    main_count = 1 if (_FIXTURE / "main.ts").exists() else 0
    total = src_count + main_count
    assert parsed_json.get("files_indexed") == total, (
        f"_seed/scip-slice.template.json files_indexed must equal "
        f"count(src/*.ts + main.ts) = {total}; got {parsed_json.get('files_indexed')!r}"
    )
    assert parsed_json.get("files_in_repo") == total, (
        f"_seed/scip-slice.template.json files_in_repo must equal "
        f"count(src/*.ts + main.ts) = {total}; got {parsed_json.get('files_in_repo')!r}"
    )


