# --- .gitignore predicate (AC-17) --------------------------------------------


def _gitignore_excludes_git_and_codegenie(raw_bytes: bytes) -> None:
    """AC-17 — .gitignore lists .git/ AND .codegenie/ verbatim.

    No `.codegenie/` carve-out: the real seed binary lives at
    ``_seed/scip-index.scip``, NOT under `.codegenie/`. S7-01's central
    no-committed-cache guard rests on this.
    """
    text = raw_bytes.decode("utf-8")
    lines = {
        line.strip()
        for line in text.splitlines()
        if line.strip() and not line.lstrip().startswith("#")
    }
    assert ".git/" in lines, f".gitignore must list `.git/`; got non-comment lines {lines!r}"
    assert ".codegenie/" in lines, f".gitignore must list `.codegenie/`; got {lines!r}"


