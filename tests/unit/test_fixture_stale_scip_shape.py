def _last_indexed_defaults_to_head_tilde_one(raw_bytes: bytes) -> None:
    """AC-20 — LAST_INDEXED defaults to HEAD~1 (the parent), never HEAD.

    Mutation: a contributor "fixes" regenerate.sh to default LAST_INDEXED
    to HEAD → this predicate fails AND the adversarial fails (the
    materialized scip.json carries last_indexed == HEAD).
    """
    text = raw_bytes.decode("utf-8")
    assert 'LAST_INDEXED="${LAST_INDEXED:-$(git rev-parse HEAD~1)}"' in text, (
        "regenerate.sh must default LAST_INDEXED to HEAD~1 (the parent), "
        "NEVER HEAD itself. This is the Risk-#3 front-line guard."
    )


def _regen_refuses_current_head(raw_bytes: bytes) -> None:
    """AC-20 — explicit guard against operator-forced LAST_INDEXED=HEAD."""
    text = raw_bytes.decode("utf-8")
    assert '[[ "$LAST_INDEXED" == "$(git rev-parse HEAD)" ]]' in text, (
        "regenerate.sh must contain an explicit check against operator-forced "
        "LAST_INDEXED=$(git rev-parse HEAD). This is the second Risk-#3 layer."
    )
    assert "exit 1" in text
    assert "refuses to set last_indexed_commit" in text


def _regen_invokes_only_allowlisted_binaries(raw_bytes: bytes) -> None:
    """AC-31 — the regen script's invoked binaries are git + coreutils only.

    The tokenizer-based test in
    ``test_fixture_stale_scip_regenerate_allowlist.py`` is the
    authoritative check; this content predicate is a textual smoke that
    `scip-typescript` and `pnpm` strings don't appear at line-start
    (they could appear in comments — that's fine).
    """
    text = raw_bytes.decode("utf-8")
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        # No regen-time invocation of scip-typescript or pnpm.
        assert not stripped.startswith("scip-typescript "), (
            f"regenerate.sh invokes scip-typescript at line-start: {line!r}. "
            "The seed-build ritual is OUT-OF-BAND."
        )
        assert not stripped.startswith("pnpm "), (
            f"regenerate.sh invokes pnpm at line-start: {line!r}. "
            "pnpm is NOT in ALLOWED_BINARIES per ADR-0001."
        )


